#Entrada
import math
dobro = int(input("Qual é o dobro de: "))
triplo = int(input("Qual é o triplo de: "))
quadrado = int(input("Qual é o quadrado de: "))

#Processamento
dobro = dobro*2
triplo = triplo*3
quadrado = math.sqrt(quadrado)

#Sáida
print ("O dobro do valor digitado é:",dobro,"o triplo do valor digitado é:",triplo,"o quadrado do valor digitado  é:",quadrado)
